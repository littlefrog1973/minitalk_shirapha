#include <stdio.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const char	*c1;
	const char	*c2;
	size_t		i;

	c1 = s1;
	c2 = s2;
	i = 0;
	while (i < n)
	{
		if (c1[i] != c2[i])
			return ((unsigned char) c1[i] - (unsigned char) c2[i]);
		i++;
	}
	return ((unsigned char) 0);
}
